  'そういえば、車磨いておこうかなピィ',
  '今日のおやつは何かなピィ〜',
  'ふんふんふん、ごきげんピィ',
  'ピピッ、そろそろお散歩の時間かもピィ',
  'うとうと…あ、起きてるピィ！',
  '羽づくろい中ピィ、ちょっと待っててピィ',
  '今日も家族のみんな元気かなピィ',
  'ピュイッ、なんとなく歌いたい気分ピィ',
  'くるくる、目が回っちゃうピィ',
  'ポピィ、今日も店長がんばるピィ！',
  'ぼーっと外を眺めてるピィ〜',
